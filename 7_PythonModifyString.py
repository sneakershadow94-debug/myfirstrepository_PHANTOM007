
# Upper case 
x = "my name is phantom and my father name is ghost"
print(x.upper())

#Lowercase
x = "MY NAME IS PHANTOM AND MY FATHER NAME IS GHOST"
print(x.lower())

# Removing Whitespace

x = "   Phantom, Ali "
print(x.strip())  

# Replace string 

x = "This is data  science tutorials"
print(x.replace("This" , "Bahi"))

# Split string 

x = "This, is, data , science, tutorials"
print(x.split(","))


